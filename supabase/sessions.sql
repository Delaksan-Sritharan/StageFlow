create extension if not exists pgcrypto;

create table if not exists public.users (
  id uuid primary key references auth.users(id) on delete cascade,
  email text not null unique,
  display_name text,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.sessions (
  id bigint generated by default as identity primary key,
  user_id uuid references public.users(id) on delete cascade,
  title text not null check (char_length(trim(title)) > 0),
  date date not null,
  created_at timestamp with time zone not null default now()
);

create table if not exists public.speakers (
  id bigint generated by default as identity primary key,
  session_id bigint not null references public.sessions(id) on delete cascade,
  name text not null check (char_length(trim(name)) > 0),
  role text not null check (role in ('Speaker', 'Evaluator', 'Table Topics')),
  min_time integer not null check (min_time >= 0),
  max_time integer not null check (max_time >= min_time),
  created_at timestamp with time zone not null default now()
);

create table if not exists public.feedback (
  id bigint generated by default as identity primary key,
  speaker_id bigint not null references public.speakers(id) on delete cascade,
  content_score integer not null check (content_score between 1 and 10),
  delivery_score integer not null check (delivery_score between 1 and 10),
  confidence_score integer not null check (confidence_score between 1 and 10),
  comment text,
  created_at timestamp with time zone not null default now()
);

alter table public.sessions add column if not exists user_id uuid references public.users(id) on delete cascade;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.users (id, email, display_name)
  values (
    new.id,
    new.email,
    nullif(trim(coalesce(new.raw_user_meta_data ->> 'display_name', '')), '')
  )
  on conflict (id) do update
  set email = excluded.email,
      display_name = coalesce(excluded.display_name, public.users.display_name);

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute procedure public.handle_new_user();

